import math #toca inportar operadores matemáticos de python
Radio=float(input("Vamos a calcular el área de un circulo, ingresa el radio: ")) #Capturar el Radio por el usuario
Area=math.pi*(Radio*Radio)# Area= pi x r`2 
print("El area de tu círculo con Radio de: ",Radio," es: ", Area)

